package Lectures.part1basics

object ValuesVariablesTypes extends App {

  val x: Int = 42
  println(x)
  var y = 54
  println(y)

  val aString: String = "Hello"
  val aBoolean: Boolean = true
  val aChar: Char = 'a'
  val anInt: Int = x
  val aShort: Short = 4234
  val aLong: Long = 3333123123L
  val aFloat: Float = 2.0f

  var z: Int = 2
  println(z)

}
